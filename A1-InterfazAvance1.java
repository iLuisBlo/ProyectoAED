    private void invertirArray(String[] array) {
    int izquierda = 0;
    int derecha = array.length - 1;
    while (izquierda < derecha) {
        String temp = array[izquierda];
        array[izquierda] = array[derecha];
        array[derecha] = temp;
        izquierda++;
        derecha--;
    }
    }
