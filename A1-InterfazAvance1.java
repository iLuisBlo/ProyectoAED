    private int[] mergeSort(boolean ascendente) {
        int n = Table.getRowCount();
        int[] indices = new int[n];
        for (int i = 0; i < n; i++) {
            indices[i] = i;
        }
        mergeSortHelper(0, n - 1, indices, ascendente);
        return indices;
    }

    private void mergeSortHelper(int low, int high, int[] indices, boolean ascendente) {
        if (low < high) {
            int mid = (low + high) / 2;
            mergeSortHelper(low, mid, indices, ascendente);
            mergeSortHelper(mid + 1, high, indices, ascendente);
            merge(low, mid, high, indices, ascendente);
        }
    }

    private void merge(int low, int mid, int high, int[] indices, boolean ascendente) {
        int[] temp = new int[high - low + 1];
        int i = low, j = mid + 1, k = 0;
        while (i <= mid && j <= high) {
            double val1 = Double.parseDouble(Table.getValueAt(indices[i], 1).toString());
            double val2 = Double.parseDouble(Table.getValueAt(indices[j], 1).toString());
            if ((ascendente && val1 <= val2) || (!ascendente && val1 >= val2)) {
                temp[k++] = indices[i++];
            } else {
                temp[k++] = indices[j++];
            }
        }
        while (i <= mid) {
            temp[k++] = indices[i++];
        }
        while (j <= high) {
            temp[k++] = indices[j++];
        }
        for (i = 0; i < temp.length; i++) {
            indices[low + i] = temp[i];
        }
        
    }

    private void actualizarTabla(int[] indices) {
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        DefaultTableModel newModel = new DefaultTableModel();
        for (int i = 0; i < model.getColumnCount(); i++) {
            newModel.addColumn(model.getColumnName(i));
        }
        for (int index : indices) {
            Object[] row = new Object[model.getColumnCount()];
            for (int j = 0; j < model.getColumnCount(); j++) {
                row[j] = model.getValueAt(index, j);
            }
            newModel.addRow(row);
        }
        Table.setModel(newModel);
    }

