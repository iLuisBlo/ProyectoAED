private int[] mergeSortColumna(int columna, boolean ascendente) {
    int n = Table.getRowCount();
    int[] indices = new int[n];
    for (int i = 0; i < n; i++) {
        indices[i] = i;
    }
    mergeSortHelperColumna(0, n - 1, indices, columna, ascendente);
    return indices;
}

private void mergeSortHelperColumna(int low, int high, int[] indices, int columna, boolean ascendente) {
    if (low < high) {
        int mid = (low + high) / 2;
        mergeSortHelperColumna(low, mid, indices, columna, ascendente);
        mergeSortHelperColumna(mid + 1, high, indices, columna, ascendente);
        mergeColumna(low, mid, high, indices, columna, ascendente);
    }
}

private void mergeColumna(int low, int mid, int high, int[] indices, int columna, boolean ascendente) {
    int[] temp = new int[high - low + 1];
    int i = low, j = mid + 1, k = 0;
    while (i <= mid && j <= high) {
        Comparable<Object> val1 = (Comparable<Object>) Table.getValueAt(indices[i], columna);
        Comparable<Object> val2 = (Comparable<Object>) Table.getValueAt(indices[j], columna);
        if ((ascendente && val1.compareTo(val2) <= 0) || (!ascendente && val1.compareTo(val2) >= 0)) {
            temp[k++] = indices[i++];
        } else {
            temp[k++] = indices[j++];
        }
    }
    while (i <= mid) {
        temp[k++] = indices[i++];
    }
    while (j <= high) {
        temp[k++] = indices[j++];
    }
    for (i = 0; i < temp.length; i++) {
        indices[low + i] = temp[i];
    }
}

