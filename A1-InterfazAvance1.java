private int[] quickSortColumna(int columna, boolean ascendente) {
    String[] nombres = new String[Table.getRowCount()];
    for (int i = 0; i < Table.getRowCount(); i++) {
        nombres[i] = (String) Table.getValueAt(i, columna);
    }

    QuickSortString.quickSort(nombres, 0, nombres.length - 1);

    if (!ascendente) {
        invertirArray(nombres); 
    }

    int[] indicesOrdenados = new int[nombres.length];
    for (int i = 0; i < nombres.length; i++) {
        for (int j = 0; j < Table.getRowCount(); j++) {
            if (nombres[i].equals(Table.getValueAt(j, columna))) {
                indicesOrdenados[i] = j;
            }
        }
    }
    return indicesOrdenados;
}
    private int busquedaBinariaPrecio(JTable table, double precioBuscado, int columnIndex) {
    TableModel model = table.getModel();
    int rowCount = model.getRowCount();
    for (int i = 0; i < rowCount; i++) {
        double precio = Double.parseDouble(model.getValueAt(i, columnIndex).toString());
        if (precio == precioBuscado) {
            return i;
        }
    }
    return -1;
    }   

    
