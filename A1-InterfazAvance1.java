    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {                                          
    String datoBuscado = jTextField1.getText().toLowerCase();
    
    String[] nombresProductos = new String[Table.getRowCount()];
    for (int i = 0; i < Table.getRowCount(); i++) {
        nombresProductos[i] = ((String) Table.getValueAt(i, 0)).toLowerCase();
    }
    Arrays.sort(nombresProductos);
    
    int index = Arrays.binarySearch(nombresProductos, datoBuscado);
    
    TextResultado.setText("Resultado de la búsqueda:\n");
    if (index >= 0) {
        for (int i = 0; i < Table.getRowCount(); i++) {
            if (nombresProductos[index].equals(((String) Table.getValueAt(i, 0)).toLowerCase())) {
                TextResultado.append("El producto '" + datoBuscado + "' se encuentra en la posición: " + (i + 1));
                return;
            }
        }
    }
    TextResultado.append("El producto '" + datoBuscado + "' no se encontró en la tabla.");   
    }                                         

